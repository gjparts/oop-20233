package pkg09_polimorfismo2;

/**
 * Representa un autor de un archivo
 * @author Gerardo Portillo
 */
public class Author {
    //attributos
    public String nombre;
    public String website;
    public String email;
    //constructor
    public Author(String nombre, String website, String email) {
        this.nombre = nombre;
        this.website = website;
        this.email = email;
    }
}
